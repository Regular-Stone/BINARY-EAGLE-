        <h2>L'association</h2>
        <p>Binary Eagle est une association Niçoise créée le 23 février 2020, à but non lucratif (se référant à la loi 1901). Elle a comme vocation d’agir dans le secteur de l’E-sport, par différentes actions (évènements/ LineUp). L’origine de sa création est due à deux constats. Tout d’abord celui lié à l’image de l'e-sport français en France et dans le monde, qui, alors que dans un premier temps se faisait timide, est actuellement entrain de progressivement émerger grâce à des évènements ponctuels ou réguliers associés à certaines villes. Et ensuite par le fait que contrairement à d'autres villes Françaises comme Montpellier, Nice n'a pas d'évènement e-sport majeur.</p>
        <h2>Nos objectifs</h2>
        <p>Binary Eagle a pour objectif de promouvoir l’e-sport à Nice et dans les alentours, en organisant des évènements, en créant une LineUp compétitive et en proposant des services de coaching. L’association a pour ambition de devenir un acteur majeur de l’e-sport dans la région, en proposant des évènements de qualité et en participant à des compétitions nationales et internationales.</p>
        <h2>Notre équipe</h2>
        <p>Binary Eagle est composée de 3 membres fondateurs, tous passionnés par l’e-sport et ayant une expérience dans le domaine. L’équipe est complétée par des membres actifs, qui participent à l’organisation des évènements et à la gestion de la LineUp. L’association est également soutenue par des partenaires locaux, qui permettent de financer les évènements et de proposer des lots aux participants.</p>
